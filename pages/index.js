import Head from 'next/head'
import Navbar from '../components/Navbar'
import ToDo from '../components/ToDo'
import { table, sortingRecords } from './api/utils/airtableHelper'
import { ToDosContext } from '../contexts/ToDosContext'
import { useEffect, useContext } from 'react'
import ToDoForm from '../components/ToDoForm'
import { useUser } from '@auth0/nextjs-auth0';

export default function Home({initialTodos}) {
  const { user } = useUser();
  const {todos, setTodos} = useContext(ToDosContext);
  useEffect(()=>{
    setTodos(initialTodos)
  },[])
  
  return (
    <div>
      <Head>
        <title>My ToDo application</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Navbar />
      <main>
        { user && 
          <>
            <h1 className='text-2xl text-center mb-4'>My ToDo application </h1>
            <ToDoForm />
            <ul>
              { todos && 
                todos.map(todo => 
                  <ToDo key={todo.id} todo={todo}/>
                )}
            </ul>
          </>
        }
      </main>
    </div>
  )
}

export async function getServerSideProps(context){
  try{
    const firstTodos = await table.select({}).firstPage();
    return {
      props: {
        initialTodos : sortingRecords(firstTodos)
      }
    }
  }catch(err){
    console.error(err)
    return{
      props : {
        err : "Something went wrong!..."
      }
    }
  }
}
